#include "libft.h"

void	*ft_calloc( size_t nmemb, size_t size )
{
	void	*array;
	size_t	max_size;

	max_size = -1;
	if (nmemb == 0 || size == 0)
		return (malloc(0));
	if (size > max_size / nmemb)
		return (NULL);
	array = malloc(nmemb * size);
	if (array == NULL)
		return (NULL);
	ft_memset(array, 0, nmemb * size);
	return (array);
}
